public class Developer {

    String description,
        dev1,
        dev2,
        dev3,
        name,
        devPic,
        appPic;

    public Developer() {}

    public String getDescription() {
        return description;
    }

    public void setDescription(String description) {
        this.description = description;
    }

    public String getDev1() {
        return dev1;
    }

    public void setDev1(String dev1) {
        this.dev1 = dev1;
    }

    public String getDev2() {
        return dev2;
    }

    public void setDev2(String dev2) {
        this.dev2 = dev2;
    }

    public String getDev3() {
        return dev3;
    }

    public void setDev3(String dev3) {
        this.dev3 = dev3;
    }

    public String getName() {
        return name;
    }

    public void setName(String name) {
        this.name = name;
    }

    public String getDevPic() {
        return devPic;
    }

    public void setDevPic(String devPic) {
        this.devPic = devPic;
    }

    public String getAppPic() {
        return appPic;
    }

    public void setAppPic(String appPic) {
        this.appPic = appPic;
    }
}
